#include <bits/stdc++.h>
using namespace std;

int main()
{
    // n = -(n + 1)
    int num1 = 35;
    int num2 = -150;
    cout << "~(" << num1 << ") = " << (~num1) << endl;
    cout << "~(" << num2 << ") = " << (~num2) << endl;

    return 0;
}